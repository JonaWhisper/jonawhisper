pub(super) fn apply_all(text: &str) -> String {
    let mut r = super::apply_regex_list(text, &RE_PCT_RU);
    r = super::apply_regex_list(&r, &RE_HOURS_RU);
    r = super::apply_regex_list(&r, &RE_CURRENCY_RU);
    r = super::apply_regex_list(&r, &RE_ORDINAL_RU);
    r = super::apply_regex_list(&r, &RE_UNITS_RU);
    apply_cardinals_ru(&r)
}

#[cfg(test)]
mod tests {
    use crate::cleanup::itn::apply_itn;

    #[test]
    fn simple_numbers() {
        assert_eq!(
            apply_itn("\u{0443} \u{043C}\u{0435}\u{043D}\u{044F} \u{043F}\u{044F}\u{0442}\u{044C} \u{043A}\u{043E}\u{0448}\u{0435}\u{043A}", "ru"),
            "\u{0443} \u{043C}\u{0435}\u{043D}\u{044F} 5 \u{043A}\u{043E}\u{0448}\u{0435}\u{043A}"
        );
        assert_eq!(
            apply_itn("\u{0434}\u{0432}\u{0435}\u{043D}\u{0430}\u{0434}\u{0446}\u{0430}\u{0442}\u{044C} \u{0447}\u{0435}\u{043B}\u{043E}\u{0432}\u{0435}\u{043A}", "ru"),
            "12 \u{0447}\u{0435}\u{043B}\u{043E}\u{0432}\u{0435}\u{043A}"
        );
    }

    #[test]
    fn hundreds() {
        assert_eq!(apply_itn("\u{0441}\u{0442}\u{043E}", "ru"), "100");
        assert_eq!(apply_itn("\u{0434}\u{0432}\u{0435}\u{0441}\u{0442}\u{0438}", "ru"), "200");
        assert_eq!(apply_itn("\u{043F}\u{044F}\u{0442}\u{044C}\u{0441}\u{043E}\u{0442}", "ru"), "500");
    }

    #[test]
    fn thousands() {
        assert_eq!(apply_itn("\u{0442}\u{044B}\u{0441}\u{044F}\u{0447}\u{0430}", "ru"), "1000");
        assert_eq!(apply_itn("\u{0434}\u{0432}\u{0435} \u{0442}\u{044B}\u{0441}\u{044F}\u{0447}\u{0438}", "ru"), "2000");
    }

    #[test]
    fn currencies() {
        assert_eq!(apply_itn("\u{043F}\u{044F}\u{0442}\u{044C} \u{0440}\u{0443}\u{0431}\u{043B}\u{0435}\u{0439}", "ru"), "5 \u{20BD}");
        assert_eq!(apply_itn("\u{0434}\u{0435}\u{0441}\u{044F}\u{0442}\u{044C} \u{0435}\u{0432}\u{0440}\u{043E}", "ru"), "10 \u{20AC}");
    }

    #[test]
    fn ordinals() {
        assert_eq!(
            apply_itn("\u{043F}\u{0435}\u{0440}\u{0432}\u{044B}\u{0439} \u{0434}\u{0435}\u{043D}\u{044C}", "ru"),
            "1-\u{0439} \u{0434}\u{0435}\u{043D}\u{044C}"
        );
    }

    #[test]
    fn percentages() {
        assert_eq!(
            apply_itn("\u{0434}\u{0435}\u{0441}\u{044F}\u{0442}\u{044C} \u{043F}\u{0440}\u{043E}\u{0446}\u{0435}\u{043D}\u{0442}\u{043E}\u{0432}", "ru"),
            "10 %"
        );
    }
}
