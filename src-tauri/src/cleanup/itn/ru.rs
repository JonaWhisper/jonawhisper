fn ru_multiplier(word: &str) -> Option<u64> {
    match word {
        "\u{0441}\u{0442}\u{043E}" => Some(100),
        "\u{0434}\u{0432}\u{0435}\u{0441}\u{0442}\u{0438}" => Some(200),
        "\u{0442}\u{0440}\u{0438}\u{0441}\u{0442}\u{0430}" => Some(300),
        "\u{0447}\u{0435}\u{0442}\u{044B}\u{0440}\u{0435}\u{0441}\u{0442}\u{0430}" => Some(400),
        "\u{043F}\u{044F}\u{0442}\u{044C}\u{0441}\u{043E}\u{0442}" => Some(500),
        "\u{0448}\u{0435}\u{0441}\u{0442}\u{044C}\u{0441}\u{043E}\u{0442}" => Some(600),
        "\u{0441}\u{0435}\u{043C}\u{044C}\u{0441}\u{043E}\u{0442}" => Some(700),
        "\u{0432}\u{043E}\u{0441}\u{0435}\u{043C}\u{044C}\u{0441}\u{043E}\u{0442}" => Some(800),
        "\u{0434}\u{0435}\u{0432}\u{044F}\u{0442}\u{044C}\u{0441}\u{043E}\u{0442}" => Some(900),
        "\u{0442}\u{044B}\u{0441}\u{044F}\u{0447}\u{0430}" | "\u{0442}\u{044B}\u{0441}\u{044F}\u{0447}\u{0438}" | "\u{0442}\u{044B}\u{0441}\u{044F}\u{0447}" => Some(1_000),
        "\u{043C}\u{0438}\u{043B}\u{043B}\u{0438}\u{043E}\u{043D}" | "\u{043C}\u{0438}\u{043B}\u{043B}\u{0438}\u{043E}\u{043D}\u{0430}" | "\u{043C}\u{0438}\u{043B}\u{043B}\u{0438}\u{043E}\u{043D}\u{043E}\u{0432}" => Some(1_000_000),
        "\u{043C}\u{0438}\u{043B}\u{043B}\u{0438}\u{0430}\u{0440}\u{0434}" | "\u{043C}\u{0438}\u{043B}\u{043B}\u{0438}\u{0430}\u{0440}\u{0434}\u{0430}" | "\u{043C}\u{0438}\u{043B}\u{043B}\u{0438}\u{0430}\u{0440}\u{0434}\u{043E}\u{0432}" => Some(1_000_000_000),
        _ => None,
    }
}

fn parse_ru_number(words: &[&str]) -> Option<(u64, usize)> {
    if words.is_empty() {
        return None;
    }
    let lower: Vec<String> = words.iter().map(|w| w.to_lowercase()).collect();
    let mut pos = 0;
    let mut total: u64 = 0;
    let mut current_group: u64 = 0;
    let mut consumed_any = false;

    while pos < lower.len() {
        let w = lower[pos].as_str();
        if let Some(val) = ru_atom(w) {
            current_group += val;
            pos += 1;
            consumed_any = true;
            continue;
        }
        if let Some(mult) = ru_multiplier(w) {
            consumed_any = true;
            if (200..=900).contains(&mult) {
                total += mult;
                pos += 1;
                continue;
            }
            if mult >= 1_000_000 {
                let coef = if current_group == 0 { 1 } else { current_group };
                total += coef * mult;
                current_group = 0;
            } else if mult == 1_000 {
                let coef = if current_group == 0 { 1 } else { current_group };
                total += coef * 1_000;
                current_group = 0;
            } else {
                let coef = if current_group == 0 { 1 } else { current_group };
                current_group = coef * 100;
            }
            pos += 1;
            continue;
        }
        break;
    }

    if !consumed_any {
        return None;
    }
    total += current_group;
    Some((total, pos))
}

